// Postfix Decrement in an Expression
// What will be the final values of a and b after this code executes?
let a = 8;
let b = a--;
console.log("a:", a); // 
console.log("b:", b); // 


// Prefix Decrement in an Expression
// What will be the final values of c and d after this code executes?
let c = 8;
let d = --c;
console.log("c:", c); // 
console.log("d:", d); // 


// Combining Postfix with Other Operators (Decrement)
// What will be the final values of e and f after this code executes?
let e = 15;
let f = e-- + 5;
console.log("e:", e); // 
console.log("f:", f); //


// Combining Prefix with Other Operators (Decrement)
// What will be the final values of g and h after this code executes?
let g = 15;
let h = --g - 3;
console.log("g:", g); // 
console.log("h:", h); //