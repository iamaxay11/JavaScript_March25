// Postfix Increment in an Expression
// What will be the final values of a and b after this code executes?
let a = 5;
let b = a++;
console.log("a:", a); // 
console.log("b:", b); // 


// Prefix Increment in an Expression
// What will be the final values of c and d after this code executes? 
let c = 5;
let d = ++c;
console.log("c:", c); // 
console.log("d:", d); // 


//Combining Postfix with Other Operators
// What will be the final values of e and f after this code executes?
let e = 20;
let f = e++ + 10;
console.log("e:", e); // 
console.log("f:", f); // 


// Combining Prefix with Other Operators
// What will be the final values of g and h after this code executes?
let g = 20;
let h = ++g - 5;
console.log("g:", g); // 
console.log("h:", h); // 
