// Basic Calculator

let a = 10 , b = 12;
let operation = '*'

switch (operation){
    case '+' : console.log(a + b); break;
    case '-' : console.log(a - b); break;
    case '*' : console.log(a * b); break;
    case '/' : console.log(a / b); break;
    default: console.log("Invalid operation");

}
